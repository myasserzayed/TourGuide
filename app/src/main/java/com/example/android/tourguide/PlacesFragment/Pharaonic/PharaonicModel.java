package com.example.android.tourguide.PlacesFragment.Pharaonic;

public class PharaonicModel {
    private int img;
    private String name;

    public PharaonicModel(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }
}
