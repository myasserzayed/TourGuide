package com.example.android.tourguide.PlacesFragment.Islamic;

public class IslamicModel {
    private int img;
    private String name;

    public IslamicModel(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }
}
